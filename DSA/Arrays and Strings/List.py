arr = [1,2,3,4,5]

print(arr[0]) # printing value at index '0'

print(arr[:-1]) # printing all the elements in array

arr.append(6) # adding an element into the array

print(arr)

arr.insert(1,12) # inserting the element 12 at postion '1'

print(arr)

arr.remove(5) # remove first occurence of element '5'

print(arr)

print(arr[1:4]) # slicing printing the elements from index '1' to '3'

